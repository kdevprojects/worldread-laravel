     * @return \Illuminate\Http\Response
     */
    public function create(StoryPostRequest $request)
    {
        $story = new Story([
            'title' => $request->title,
            'summary' => $request->summary,
            'body' => $request->body,
            'author_id' => Auth::user()->id
        ]);
        $story->save();
        return response()->json([
            'message' => 'success'
        ]);
    }

